import os
from django.core.asgi import get_asgi_application
from channels.routing import ProtocolTypeRouter, URLRouter
from channels.auth import AuthMiddlewareStack
import chat_project.chat.routing
from chat_project import chat

os.environ.setdefault('DJANGO_SETTINGS_MODULE', 'chat_project.settings')

application = ProtocolTypeRouter({
    "http": get_asgi_application(),
    "websocket": AuthMiddlewareStack(  # AuthMiddlewareStack handles WebSocket authentication
        URLRouter(
            chat.routing.websocket_urlpatterns  # Routing defined in chat/routing.py
        )
    ),
})
